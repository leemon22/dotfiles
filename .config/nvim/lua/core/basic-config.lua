local set = vim.opt

set.title = true			-- titulo
set.number = true			-- mostrar # de linea
set.relativenumber = true	-- mostrar # relativo de linea
set.mouse = 'a'				-- integracion de mouse

set.wrap = false			-- no dividir lineas largas

set.cursorline = true		-- mostrar la linea del cursor

set.tabstop = 4				-- espacios tabulacion
set.shiftwidth = 4			-- espacios autoidentacion
set.softtabstop = 4			-- espacios que ocupa un tab

set.ignorecase = true		-- ignorar mayusculas en busquedas
set.smartcase = true		-- no ignorar mayusculas si el termino de busqueda contiene mayusculas

set.spelllang = 'en,es'		-- Idiomas

set.termguicolors = true	-- Terminal mas colorido
